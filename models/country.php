    function findISO2($code) {
        $code = strtoupper($code);
        $iso2 = $this->find('first', array(
            'conditions' => array(
                'Country.iso' => $code
            ),
            'fields' => array(
                'Country.iso'
            )
        ));
        if ($iso2 !== false) {
            return $iso2['Country']['iso'];
        }
        $iso2 = $this->find('first', array(
            'conditions' => array(
                'Country.iso3' => $code
            ),
            'fields' => array(
                'Country.iso'
            )
        ));
        if ($iso2 !== false) {
            return $iso2['Country']['iso'];
        }
        if (is_numeric($code)) {
            $iso2 = $this->find('first', array(
                'conditions' => array(
                    'Country.iso_numeric' => (int)$code
                ),
                'fields' => array(
                    'Country.iso'
                )
            ));
            if ($iso2 !== false) {
                return $iso2['Country']['iso'];
            }
        }
        return '00';
    }

