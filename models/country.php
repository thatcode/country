    function countryName($code) {
        $iso2 = $this->findISO2($code);
        $data = $this->find('first', array(
            'conditions' => array(
                'Country.iso' => $iso2
            ),
            'fields' => array(
                'Country.name'
            )
        ));
        return $data['Country']['name'];
    }

    function continentName($code) {
        $iso2 = $this->findISO2($code);
        $this->recursive = 0;
        $data = $this->find('first', array(
            'conditions' => array(
                'Country.iso' => $iso2
            ),
            'fields' => array(
                'Country.continent_id',
                'Continent.name'
            )
        ));
        return $data['Continent']['name'];
    }

